const create2Darr = (row, col) => {
  const temp = [];
  for (let i = 0; i < row; i++) {
    const colitems = [];
    for (let j = 0; j < col; j++) {
      colitems.push(0);
    }
    temp.push(colitems);
  }
  return temp;
};

const randomArr = (arr) => {
  for (const element of arr) {
    for (let j = 0; j < element.length; j++) {
      let ran = Math.random();
      if (ran > 0.7) {
        element[j] = 1;
      } else {
        element[j] = 0;
      }
    }
  }
  return arr;
};

const nextRound = (arr) => {
  const copy = arr.map((subarr) => subarr.slice());
  const isLiving = (array, row, col) => {
    if (row < 0 || col < 0 || col >= array[0].length || row >= array.length)
      return 0;
    return array[row][col];
  };
  const checkLiving = (_arr, i, j) => {
    return (
      isLiving(_arr, i - 1, j - 1) +
      isLiving(_arr, i - 1, j) +
      isLiving(_arr, i - 1, j + 1) +
      isLiving(_arr, i, j + 1) +
      isLiving(_arr, i + 1, j + 1) +
      isLiving(_arr, i + 1, j) +
      isLiving(_arr, i + 1, j - 1) +
      isLiving(_arr, i, j - 1)
    );
  };
  for (let i = 0; i < arr.length; i++) {
    for (let j = 0; j < arr[i].length; j++) {
      if (arr[i][j] === 0 && checkLiving(arr, i, j) === 3) {
        copy[i][j] = 1;
      }
      if (
        arr[i][j] === 1 &&
        (checkLiving(arr, i, j) < 2 || checkLiving(arr, i, j) > 3)
      ) {
        copy[i][j] = 0;
      }
    }
  }
  return copy;
};
